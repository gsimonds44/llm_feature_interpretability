"""
This script runs a GUI which allows searching for desired features. 

All outputs generated by the llm are shown in a general text format, and are scrollable using the up/down arrow keys
or the left-hand scrollbar.

Tokens (text) are shown in a colormap indicating the activation level for the selected feature.

All feature activations may be visualized by using the mousewheel to scroll. 

A key-token, or muliple, may be highlighted/unhighlighted by left clicking them. This will limit the scrollable features to those
whose selected tokens fall above the activation threshold. Activation threshold can be set using the right-hand scrollbar.
"""


